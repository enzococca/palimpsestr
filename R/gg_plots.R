.theme_sef <- function(base_size = 11) {
  ggplot2::theme_minimal(base_size = base_size) %+replace%
    ggplot2::theme(
      plot.title = ggplot2::element_text(face = "bold", size = base_size + 3, hjust = 0),
      plot.subtitle = ggplot2::element_text(colour = "grey40", size = base_size, hjust = 0,
                                             margin = ggplot2::margin(b = 10)),
      plot.caption = ggplot2::element_text(colour = "grey50", size = base_size - 2, hjust = 1,
                                            face = "italic"),
      panel.grid.minor = ggplot2::element_blank(),
      panel.grid.major = ggplot2::element_line(colour = "grey92"),
      legend.position = "right",
      legend.title = ggplot2::element_text(face = "bold", size = base_size - 1),
      axis.title = ggplot2::element_text(face = "bold"),
      plot.margin = ggplot2::margin(15, 15, 10, 10)
    )
}

.sef_caption <- function() {
  paste0("Generated by palimpsestr ", as.character(utils::packageVersion("palimpsestr")))
}

# ── Colour palettes (colour-blind safe) ──

.phase_colours <- function(k) {
  pal <- c("#E69F00", "#56B4E9", "#009E73", "#CC79A7", "#D55E00", "#0072B2", "#F0E442", "#999999")
  pal[seq_len(min(k, length(pal)))]
}

.viridis_c <- function(name, option = "viridis", direction = 1) {
  tryCatch(
    ggplot2::scale_colour_viridis_c(name = name, option = option, direction = direction),
    error = function(e) ggplot2::scale_colour_gradient(low = "#440154", high = "#FDE725", name = name)
  )
}

.viridis_f <- function(name, option = "viridis", direction = 1) {
  tryCatch(
    ggplot2::scale_fill_viridis_c(name = name, option = option, direction = direction),
    error = function(e) ggplot2::scale_fill_gradient(low = "#440154", high = "#FDE725", name = name)
  )
}


