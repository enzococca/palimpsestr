#' Phase assignment map
#'
#' Plots artefact positions coloured by dominant phase, with point size
#' proportional to assignment confidence.
#'
#' @param object A \code{sef_fit} object.
#' @param xlabel,ylabel Axis labels (default: "Easting (m)" / "Northing (m)").
#' @return A ggplot object.
#' @seealso \code{\link{plot_phasefield}} for base R version,
#'   \code{\link{as_plotly}} for interactive version.
#' @family plotting
#' @export
gg_phasefield <- function(object, xlabel = "Easting (m)", ylabel = "Northing (m)") {
  if (!inherits(object, "sef_fit")) stop("object must be a sef_fit", call. = FALSE)
  .check_ggplot()
  df <- data.frame(
    x = object$data[[object$coords[1]]],
    y = object$data[[object$coords[2]]],
    phase = factor(object$phase),
    confidence = apply(object$phase_prob, 1, max)
  )
  k <- object$k
  n <- nrow(df)

  ggplot2::ggplot(df, ggplot2::aes(x = .data$x, y = .data$y,
                                    colour = .data$phase, size = .data$confidence)) +
    ggplot2::geom_point(alpha = 0.8, stroke = 0.3) +
    ggplot2::scale_colour_manual(
      values = .phase_colours(k), name = "Depositional\nPhase",
      labels = paste0("Phase ", seq_len(k), " (n=", table(df$phase), ")")
    ) +
    ggplot2::scale_size_continuous(
      range = c(1.5, 5), name = "Assignment\nConfidence",
      labels = function(x) sprintf("%.0f%%", x * 100)
    ) +
    ggplot2::coord_equal() +
    ggplot2::labs(
      title = "Dominant Phase Assignment",
      subtitle = sprintf("%d finds assigned to %d depositional phases | PDI = %.3f",
                         n, k, pdi(object)),
      x = xlabel, y = ylabel,
      caption = .sef_caption()
    ) +
    .theme_sef()
}


#' Spatial entropy map
#'
#' Shows Shannon entropy of phase probabilities across the excavation area.
#' High entropy = uncertain phase assignment (possible palimpsest zone).
#'
#' @param object A \code{sef_fit} object.
#' @param xlabel,ylabel Axis labels.
#' @return A ggplot object.
#' @seealso \code{\link{plot_entropy}} for base R version,
#'   \code{\link{as_plotly}} for interactive version.
#' @family plotting
#' @export
